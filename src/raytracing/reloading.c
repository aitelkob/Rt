#include "rtv1.h"

void        reload(t_mlix *mlx)
{
    int     i;
    t_vector    rgb;

    i = 0;
    while (i < WIN_W * WIN_H)
    {
        rgb = mlx->colors[i];
        mlx->img[i] = rgb_to_int(rgb);
        i++;
    }
}
